    Integer getsks(){
        DefaultTableModel tableModel;
        tableModel = (DefaultTableModel) showtabellaporan.getModel();  
         int total=0;
        for(int row=0;row<tableModel.getRowCount();row++){
            int tambah = Integer.parseInt((String)tableModel.getValueAt(row, 2));
            total += tambah;
        }
      return total;
    }
    
    private double getsksindeks() {
        DefaultTableModel tableModel;
        tableModel = (DefaultTableModel) showtabellaporan.getModel();
        
        double angka = 0 ;
        for (int row = 0; row < tableModel.getRowCount(); row++) {
            double hasil = Double.parseDouble((String) tableModel.getValueAt(row, 6));
           angka += hasil;
            
        }
        return angka;
    }
    
    private double nilaiIPS(){
        
        double ipk = getsksindeks();
        double totalsks = getsks();
              
        return ipk / totalsks;
    }
    
   
    private double nilaiIPK(double hitung){
       String angka = pilihansemesterlaporan.getSelectedItem().toString();
       double ips = hitung;
       double hasil = 0;
       if(angka == "satu"){
           hasil +=1;
           return ips /= hasil;
       }else if(angka == "dua"){
           hasil +=2;
           return ips /=hasil;
       }
       return hasil;
    }
    
